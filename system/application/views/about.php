<h1>About this App</h1>

<p>This is a sample application built to illustrate some of the uses of Gnip.</p>

<p>When entered into the form, valid and public Twitter usernames are dynamically added to a filter created at Gnip. Once each actor rule has been added to the filter, their tweets will be posted to a listener URL on this application, which then takes and processes the data into a database table so that it can be paged through from the web front end.</p>

<p>The source code for this app can be found on <a href="http://github.com/electromute/gnip-tweetgroups/">GitHub</a>.</p>

